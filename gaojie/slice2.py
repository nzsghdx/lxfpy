#!/usr/bin/env python3

print('slice操作数列:\n')

L=list(range(100))

print("索引小于等于10:'L[:10]'\n",L[:10])

print("后十个数:'L[-10:]'\n",L[-10:])

print("第十一个数是10,第二十个数是19\n")
print("第十一到第二十个数:'L[10:20]'",L[10:20])

print("前十个每两个取一个:'L[:10:2]'",L[:10:2]);

print("所有数每五个取一个:'L[::5]'",L[::5])
A=L[:]
print("复制一个:'A=L[:]'",A)

